app.handle('get_idols_by_birthday', conv => {
  const birthday = get_Japan_day(conv.session.params.birthday.month, conv.session.params.birthday.day);
  const attr = conv.session.params.attr;
  
  //指定された誕生日のアイドルを全員探す。
  let count = 0;
  let text = birthday + 'が誕生日のアイドルは';
  for(let name in idols){
    const idol = idols[name];
    if (idol['誕生日'] == birthday){
      text += '、' + idol['名前'] + 'さん';
      count += 1;
    }
  }

  if (count > 0){
  	text += 'です。';
  } else {
    text += '見つかりませんでした。';
  }
  conv.add(text);
  
  clear_params(conv);
});

